引くとは分かっておらず、ボーイング機の不良かもしれないといった話が出ていた程度のようです。<br /><br />上記のマレーシア航空のサイトでは、<br /><a href="http://sankei.jp.msn.com/world/news/140308/asi14030810040001-n1.htm" target="_blank" title="マレーシア航空特設ページ">特設ページ</a>が設置されており、<a href="http://www.malaysiaairlines.com/content/dam/malaysia-airlines/mas/PDF/MH370/Malaysia%20Airlines%20Flight%20MH%20370%20Passenger%20Manifest_Nationality@10Apr.pdf" target="_blank" title="乗客リスト">乗客リスト</a>も公開されていました。<br /><br />国籍別に載せると、<br />マレーシア　　　38名<br />インドネシア　　7名<br />オーストラリア　6名<br />インド　　　　　5名<br />フランス　　　　4名<br />アメリカ　　　　3名（孫と子供と思われる）<br />オーストリア　　1名<br />カナダ　　　　　2名<br />ニュージーランド　2名<br />ウクライナ　　　2名<br />ロシア　　　　　1名<br />オランダ　　　　1名<br />イタリア　　　　1名<br />となっていました。<br />同じ姓の人が多かったので、家族連れが多く乗っていたのでしょうか。<br /><br />続きます。<div class="fc2_footer" style="text-align:left;vertical-align:middle;height:auto;">
